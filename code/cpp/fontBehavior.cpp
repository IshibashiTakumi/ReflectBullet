CFontBehavior::CFontBehavior() : CBehavior()
{
	
}

//=============================================================================
// クラスの破棄
//=============================================================================
CFontBehavior::~CFontBehavior()
{

}

//=============================================================================
// 初期化
//=============================================================================
void CFontBehavior::Init(CFont* Font, D3DXCOLOR color, float interval)
{
	m_Font = Font;
	m_Color = color;
	m_Interval = interval;
	m_Counter = 0.0f;
}

//=============================================================================
// 解放
//=============================================================================
void CFontBehavior::Uninit(void)
{

}

//=============================================================================
// 更新
//=============================================================================
void CFontBehavior::Update(void)
{
	m_Font->SetColor(D3DXCOLOR(m_Color.r, m_Color.g, m_Color.b, sinf(m_Counter) * m_Color.a));
	m_Counter += m_Interval;
	if (m_Counter > D3DX_PI) m_Counter = 0.0f;
}
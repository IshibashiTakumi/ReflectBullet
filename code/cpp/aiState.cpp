//=============================================================================
// 状態の変更
//=============================================================================
void CAiState::Change(CAiState * AiState, CAiState * _AiState)
{
	CAiBehavior* AiBehavior = CAiBehavior::Find<CAiBehavior>(AiState->m_Object->GetBehaviorList());
	AiBehavior->m_AiState->Uninit();
	delete AiBehavior->m_AiState;
	AiBehavior->m_AiState = _AiState;
}


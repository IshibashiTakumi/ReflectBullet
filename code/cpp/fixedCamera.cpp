//=============================================================================
// 初期化
//=============================================================================
void CFixedCamera::Init(D3DXVECTOR3 PosV, D3DXVECTOR3 PosR)
{
	*m_PosR = PosR, *m_PosV = PosV;

	*m_Rot = D3DXVECTOR3(atan2f(PosV.y - PosR.y, PosV.z - PosR.z),
		atan2f(PosV.x - PosR.x, PosV.z - PosR.z),
		atan2f(PosV.y - PosR.y, PosV.x - PosR.x));
}


//=============================================================================
// クラスの生成
//=============================================================================
CShaderShadow::CShaderShadow()
{

}

//=============================================================================
// クラスの破棄
//=============================================================================
CShaderShadow::~CShaderShadow()
{

}

//=============================================================================
// 描画開始
//=============================================================================
void CShaderShadow::Begin(int pass, D3DXMATRIX& world)
{
	// エフェクト設定
	m_Effect->SetMatrix("g_world", &world);

	m_Effect->Begin(NULL, 0);	// テクニック開始 
	m_Effect->BeginPass(pass);		//パスの指定
}

//=============================================================================
// 描画終了
//=============================================================================
void CShaderShadow::End(void)
{
	m_Effect->EndPass();
	m_Effect->End();
}
